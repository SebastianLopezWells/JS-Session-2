let i;

//start point ; stop condition ; increment

for(i=0;i<5;i++){
    console.log(i);
}


//Display from 1 to 10
console.log("1-10")
for(i=0;i<=10;i++){
    console.log(i);
}

//While
let j=1;
console.log("while 1-10");
while(j<11){
    console.log(j);
    j++;
}

//do-while
let k=1;
console.log("do-while 0-10");
do{
    console.log(k);
    k++;
}while(k<11);


//While decrementing the loop counter by one iteration
j=99;
console.log("while 99-1");
while(j>0){
    console.log(j);
    j--;
}
